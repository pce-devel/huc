void out_ins (int code, int type, intptr_t data)
{
	INS tmp;

	memset(&tmp, 0, sizeof(INS));

	tmp.ins_code = code;
	tmp.ins_type = type;
	tmp.ins_data = data;
	gen_ins(&tmp);
}

