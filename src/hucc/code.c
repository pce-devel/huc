/* ----
 * gen_asm()
 * ----
 * generate optimizer asm code
 *
 */
void gen_asm (INS *inst)
{
//	int type = inst->ins_type;
//	intptr_t data = inst->ins_data;

	/* i-codes for 32-bit longs */

	switch (inst->ins_code) {

	case X_LDD_I:
		ot("__ldd_i\t\t");
		outdec((int)inst->ins_data);
		outstr(", ");
		prefix();
		outstr(inst->arg[0]);
		outstr(", ");
		prefix();
		outstr(inst->arg[1]);
		nl();
		break;

	case X_LDD_W:
		ot("__ldd_w\t\t");
		outsymbol((SYMBOL *)inst->ins_data);
		outstr(", ");
		prefix();
		outstr(inst->arg[0]);
		outstr(", ");
		prefix();
		outstr(inst->arg[1]);
		nl();
		break;

	case X_LDD_B:
		ot("__ldd_b\t\t");
		outsymbol((SYMBOL *)inst->ins_data);
		outstr(", ");
		prefix();
		outstr(inst->arg[0]);
		outstr(", ");
		prefix();
		outstr(inst->arg[1]);
		nl();
		break;

	case X_LDD_S_W:
		ot("__ldd_s_w\t");
		outdec((int)inst->ins_data);
		outstr(", ");
		prefix();
		outstr(inst->arg[0]);
		outstr(", ");
		prefix();
		outstr(inst->arg[1]);
		nl();
		break;

	case X_LDD_S_B:
		ot("__ldd_s_b\t");
		outdec((int)inst->ins_data);
		outstr(", ");
		prefix();
		outstr(inst->arg[0]);
		outstr(", ");
		prefix();
		outstr(inst->arg[1]);
		nl();
		break;

	default:
		error("internal error: invalid instruction");
		break;
	}
}
